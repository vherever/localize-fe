export class RegexpPatterns {
  get emailPattern(): string {
    return [
      '^(([^<>()\\[\\]\\\\.,;:\\s@"]+(\\.[^<>()\\[\\]\\\\.,;:\\s@"]+)*)|',
      '(".+"))@((\\[[0-9]{1,3}\\.[0-9]{1,3}\\.[0-9]{1,3}\\.[0-9]{1,3}])|',
      '(([a-zA-Z\\-0-9]+\\.)+[a-zA-Z]{2,}))$',
    ].join('');
  }

  get passwordPattern(): string {
    return '^((?=.*\\d)|(?=.*\\W+))(?![.\\n])(?=.*[A-Z])(?=.*[a-z]).*$';
  }
}
